'''
Tipo float == Tipo real, decimal (casas decimais)

valor = 1.44
print(valor)
print(type(valor))

'''

valor = 1.44
print(valor)
print(type(valor))

# é possível fazer dupla atribuição:

valor1, valor2 = 1, 44
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

# Podemos converter um float para um int:
# Obs: ao converter float para inteiro, nós perdemos precisão.
resultado = int(valor)
print(resultado)
print(type(resultado))

# Podemos trabalhar com números complexos:
variavel = 5j
print(type(variavel))

